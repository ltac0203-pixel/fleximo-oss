    /*
    |--------------------------------------------------------------------------
    | アプリケーションURL
    |--------------------------------------------------------------------------
    |
    | このURLは、Artisanコマンドラインツールを使用する際に、コンソールが
    | URLを適切に生成するために使用されます。Artisanコマンド内で使用できる
