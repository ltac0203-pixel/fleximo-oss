<?php

declare(strict_types=1);

use Illuminate\Cookie\Middleware\EncryptCookies;
use Illuminate\Foundation\Http\Middleware\ValidateCsrfToken;
use Laravel\Sanctum\Http\Middleware\AuthenticateSession;
use Laravel\Sanctum\Sanctum;

return [

    /*
    |--------------------------------------------------------------------------
    | ステートフルドメイン
    |--------------------------------------------------------------------------
    |
    | 以下のドメイン/ホストからのリクエストは、ステートフルAPI認証クッキーを
    | 受け取ります。通常、これにはフロントエンドSPAを介してAPIにアクセスする
    | ローカルおよび本番環境のドメインが含まれる必要があります。
    |
    */

    'stateful' => explode(',', env('SANCTUM_STATEFUL_DOMAINS', sprintf(
        '%s,%s',
        'localhost,localhost:3000,localhost:8000,127.0.0.1,127.0.0.1:8000,::1',
        Sanctum::currentApplicationUrlWithPort(),
    ))),

    /*
    |--------------------------------------------------------------------------
    | Sanctumガード
    |--------------------------------------------------------------------------
    |
    | この配列には、Sanctumがリクエストを認証しようとするときに
    | チェックされる認証ガードが含まれます。これらのガードのいずれも
    | リクエストを認証できない場合、Sanctumは着信リクエストに存在する
    | ベアラートークンを認証に使用します。
    |
    */

    'guard' => ['web'],

    /*
    |--------------------------------------------------------------------------
    | 有効期限（分）
    |--------------------------------------------------------------------------
    |
    | この値は、発行されたトークンが期限切れと見なされるまでの分数を
    | 制御します。これはPersonal Access Tokensのみに適用され、
    | セッションベースの認証には影響しません。
    |
    | デフォルト: 480分（8時間）
    | 根拠: 飲食店の最長シフト（8時間）をカバーしつつ、端末紛失時の
    | リスク露出を最小化する防御的設定。現在はセッション認証のみ使用
    | しているが、将来のAPI拡張に備えた安全なデフォルト値。
    | 推奨範囲: 120〜480分（セッション有効期限以上、業務シフト以内）
    |
    */

    'expiration' => env('SANCTUM_EXPIRATION', 480),

    /*
    |--------------------------------------------------------------------------
    | トークンプレフィックス
    |--------------------------------------------------------------------------
    |
    | Sanctumは、開発者がトークンをリポジトリにコミットした場合に
    | 通知する、オープンソースプラットフォームが維持する多数のセキュリティ
    | スキャンイニシアチブを活用するために、新しいトークンに
    | プレフィックスを付けることができます。
    |
    | 参照: https://docs.github.com/en/code-security/secret-scanning/about-secret-scanning
    |
    */

    'token_prefix' => env('SANCTUM_TOKEN_PREFIX', ''),

    /*
    |--------------------------------------------------------------------------
    | Sanctumミドルウェア
    |--------------------------------------------------------------------------
    |
    | Sanctumでファーストパーティ SPAを認証する場合、リクエストの処理中に
    | Sanctumが使用するミドルウェアの一部をカスタマイズする必要がある
    | 場合があります。必要に応じて、以下にリストされているミドルウェアを
    | 変更できます。
    |
    */

    'middleware' => [
        'authenticate_session' => AuthenticateSession::class,
        'encrypt_cookies' => EncryptCookies::class,
        'validate_csrf_token' => ValidateCsrfToken::class,
    ],

];
